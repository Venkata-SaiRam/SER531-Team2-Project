package com.SER531.crimeRateAnalysis.beans;

public class PropertyCrimeRates {
    private int propertyCrimeValue;
    private int burglaryCrimeValue;
    private int motorVehicleCrimeValue;
    private int arsonCrimeValue;
    private int larcenyCrimeValue;

    public int getPropertyCrimeValue() {
        return propertyCrimeValue;
    }

    public void setPropertyCrimeValue(int propertyCrimeValue) {
        this.propertyCrimeValue = propertyCrimeValue;
    }

    public int getBurglaryCrimeValue() {
        return burglaryCrimeValue;
    }

    public void setBurglaryCrimeValue(int burglaryCrimeValue) {
        this.burglaryCrimeValue = burglaryCrimeValue;
    }

    public int getMotorVehicleCrimeValue() {
        return motorVehicleCrimeValue;
    }

    public void setMotorVehicleCrimeValue(int motorVehicleCrimeValue) {
        this.motorVehicleCrimeValue = motorVehicleCrimeValue;
    }

    public int getArsonCrimeValue() {
        return arsonCrimeValue;
    }

    public void setArsonCrimeValue(int arsonCrimeValue) {
        this.arsonCrimeValue = arsonCrimeValue;
    }

    public int getLarcenyCrimeValue() {
        return larcenyCrimeValue;
    }

    public void setLarcenyCrimeValue(int larcenyCrimeValue) {
        this.larcenyCrimeValue = larcenyCrimeValue;
    }
}
